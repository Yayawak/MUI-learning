const QuiltedImageList = () => {
  function srcset(image, size, rows=1, cols=1){
    return {
      // src: `${image}` //? This is very lag maybe it's re-download image every second
      src: `${image}?w=${size*cols}&h=${size*rows}&fit=crop&auto=format`,
      srcSet: `${image}?w=${size * cols}&h=${ size * rows }&fit=crop&auto=format&dpr=2 2x`,
    }
  }
  return (
    <>
      <Box display='flex' alignItems='center' justifyContent='center' >
        <ImageList 
          sx={{width:800, height:600}} variant='quilted' cols={4} rowHeight={150} >
          {
            itemData_quilted.map((item) => (
              <ImageListItem key={item.img} cols={item.cols || 1}
                rows={item.rows || 1}>
                <img {...srcset(item.img, 120, item.rows, item.cols)} loading='lazy' />
              </ImageListItem>
            ))
          }
        </ImageList>
      </Box>
    </>
  )
}
const ImageListWithTitlebar = () => {
  return (
    <>
      <Box sx={{display:'flex', justifyContent:'center' }}>
        <ImageList sx={{width:700, height:900, }} >
          <ImageListItem key="Subheader" cols={2}>
            <ListSubheader sx={{fontSize:'20px'}}>Food Title</ListSubheader>
          </ImageListItem>
          {
            itemData_with_titlebar.map((item) => {
              return (
                <ImageListItem key={item.img}>
                  <img
                    src={`${item.img}?w=248&fit=crop&auto=format`}
                    srcSet={`${item.img}?w=248&fit=crop&auto=format&dpr=2 2x`}
                    alt={item.title}
                    loading='lazy'
                  />
                  <ImageListItemBar
                    title={item.title}
                    subtitle={item.author}
                    actionIcon={
                    <IconButton sx={{color: 'rgba(255,255,255, .5)'}}
                      aria-label={`info about ${item.title}`}>
                      <InfoIcon/>
                    </IconButton> 
                    }
                  >
                  </ImageListItemBar>
                </ImageListItem>
              )
            })
          }
        </ImageList>

      </Box>
    </>
  )
}
